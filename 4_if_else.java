class If_else {

    public static void main(String args[]){
        int x = 5;
        int y = 3;

        if (x>y) {
            System.out.println("x greater than y");
            y = 7;
            System.out.println(y);
        }

        else {
            System.out.println("y is greater or equal");
        }

    }
}
